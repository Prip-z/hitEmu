#[macroquad::main("hitEmu")]
async fn main() {
    env_logger::init();

    log::info!("Эмулятор запускается");
    // let cartidge_data = cartridge::Cartridge::parse_rom("C:\\Users\\Admin\\Desktop\\hitEmu\\Donkey Kong (World) (Rev 1).nes");
    let cartidge_data = cartridge::Cartridge::parse_rom("C:\\Users\\Admin\\Desktop\\hitEmu\\Super Mario Bros. (World).nes");

    let mut console = Console {
        ram: console::Ram { ram: vec![0; 0x0800] },
        ppu: ppu::Ppu::new(&cartidge_data),
        cartridge: cartidge_data,
        joypad: joypad::Joypad::new(),
    };
    let mut cpu = cpu::Cpu::new();
    
    cpu.internal_reset(&mut console);

    let mut screen_image = Image::gen_image_color(256, 240, BLACK);
    let texture = Texture2D::from_image(&screen_image);
    texture.set_filter(FilterMode::Nearest);
    
    loop {
        clear_background(BLACK);

        let mut ppu_cycles = 0u32;
        while ppu_cycles < 89342 {
            let cpu_cycles = cpu.make_step_and_return_cycles(&mut console);
            let ppu_steps = cpu_cycles * 3;
            for _ in 0..ppu_steps {
                console.ppu.step();
                if console.ppu.nmi_interrupt {
                    console.ppu.nmi_interrupt = false;
                    cpu.nmi(&mut console);
                }
            }
            ppu_cycles += ppu_steps as u32;
        }

        for y in 0..240 {
            for x in 0..256 {
                let color_index = console.ppu.screen_buffer[y * 256 + x] as usize;
                let rgb = NES_PALETTE[color_index & 0x3F];
                screen_image.set_pixel(x as u32, y as u32, Color::from_rgba(rgb.0, rgb.1, rgb.2, 255));
            }
        }

        texture.update(&screen_image);

        draw_texture_ex(
            &texture,
            0.0,
            0.0,
            WHITE,
            DrawTextureParams {
                dest_size: Some(vec2(screen_width(), screen_height())),
                ..Default::default()
            },
        );

        next_frame().await;
    }
}